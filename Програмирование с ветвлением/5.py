d = int(input('Введите номер дня недели',end = " "),end = " ")
if d == 1:
    print('Понедельник',end = " ")
elif d == 2:
    print('Вторник',end = " ")
elif d == 3:
    print('Среда',end = " ")
elif d == 4:
    print('Четверг',end = " ")
elif d == 5:
    print('Пятница',end = " ")
elif d == 6:
    print('Суббота',end = " ")
elif d == 7:
    print('Воскресенье',end = " ")
else:
    print('Error',end = " ")